def add_missing_translations(resource_file, lang_code):
    """Add missing translations to a resource file."""
    print(f"\nProcessing {lang_code}...")

    # Parse both files
    tree_base = ET.parse("src/Melodee.Blazor/Resources/SharedResources.resx")
    root_base = tree_base.getroot()

    tree_lang = ET.parse(resource_file)
    root_lang = tree_lang.getroot()

    # Get existing keys
    base_keys = {
        data.get("name"): data.find("value").text
        for data in root_base.findall("data")
        if data.find("value") is not None
    }

    existing_keys = {data.get("name") for data in root_lang.findall("data")}

    # Get translations for this language
    translations = TRANSLATIONS.get(lang_code, {})

    # Add missing entries
    added_count = 0
    for key in sorted(base_keys.keys()):
        if key not in existing_keys:
            # Get translation from dictionary, or use English as fallback
            translated_value = translations.get(key, base_keys[key])

            # Create new data element
            data_elem = ET.Element("data", {"name": key, "xml:space": "preserve"})
            value_elem = ET.SubElement(data_elem, "value")
            value_elem.text = translated_value

            # Add to root
            root_lang.append(data_elem)
            added_count += 1

    if added_count > 0:
        # Save with pretty formatting
        tree_lang.write(resource_file, encoding="utf-8", xml_declaration=True)
        print(f"  Added {added_count} translations to {lang_code}")
    else:
        print(f"  No missing translations in {lang_code}")

    return added_count


def main():
    """Main function to process all language files."""
    print("=" * 60)
    print("Completing Melodee.Blazor Translations")
    print("=" * 60)

    languages = {
        "es-ES": "src/Melodee.Blazor/Resources/SharedResources.es-ES.resx",
        "ru-RU": "src/Melodee.Blazor/Resources/SharedResources.ru-RU.resx",
        "zh-CN": "src/Melodee.Blazor/Resources/SharedResources.zh-CN.resx",
        "fr-FR": "src/Melodee.Blazor/Resources/SharedResources.fr-FR.resx",
        "ar-SA": "src/Melodee.Blazor/Resources/SharedResources.ar-SA.resx",
    }

    total_added = 0
    for lang_code, resource_file in languages.items():
        count = add_missing_translations(resource_file, lang_code)
        total_added += count

    print("\n" + "=" * 60)
    print(f"Translation Complete! Total translations added: {total_added}")
    print("=" * 60)


if __name__ == "__main__":
    main()
